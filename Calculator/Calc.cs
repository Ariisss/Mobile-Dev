namespace Calculator
{
    internal class Calc
    {
        public double Sum(double x, double y)
        {
            return x + y;
        }

        public double Difference(double x, double y)
        {
            return x - y;
        }

        public double Product(double x, double y)
        {
            return x * y;
        }

        public double Quotient(double x, double y)
        {
            return x / y;
        }
    }
}
